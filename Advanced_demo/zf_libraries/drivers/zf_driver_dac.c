void dac_init (void)
{
    g_dac0.p_api->open(g_dac0.p_ctrl, g_dac0.p_cfg);
    g_dac0.p_api->start(g_dac0.p_ctrl);
    gpio_set_level(DAC_EN, 1);
}


void dac_out (uint16 value)
{
    g_dac0.p_api->write(g_dac0.p_ctrl, value);
}

